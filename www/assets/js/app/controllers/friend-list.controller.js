angular
	.module('Starter')
	.controller('FriendListController', FriendListController);

function FriendListController($scope, $http) {

	////////////////////////////////////////////////////////////////////////////////////
	///
	/// MODEL
	///
	////////////////////////////////////////////////////////////////////////////////////

	$scope.friends = [];

	////////////////////////////////////////////////////////////////////////////////////
	///
	/// METHOD
	///
	////////////////////////////////////////////////////////////////////////////////////

	// Get data from json
	function getFriends() {
		$http.get('friends.json').then(function(response) {
	        $scope.friends = response.data;
	    });
	}

	getFriends();
}